/**
 * @param {number} n
 * @return {number}
 */
var totalNQueens = function(n) {
    let num = 0;
    let dfs = (list) => {
        if (list.length === n) {
            num ++;
        } else {
            for (let i = 0; i < n; i ++) {
                if (!list.includes(i)) {
                    let result = list.some((value, index) => {
                        return Math.abs(list.length - index) === Math.abs(i - value);
                    })
                    if (!result) {
                        let newList = list.concat(i);
                        dfs(newList);
                    }
                }
            }
        }
    };
    dfs([])
    return num
};
