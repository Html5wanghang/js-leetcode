/**
 * @constructor
 * @param {NestedInteger[]} nestedList
 */
var NestedIterator = function(nestedList) {
    this.stack = [];
    let dfs = (list) => {
      for (let i = list.length - 1; i >= 0; i --) {
        if (list[i].isInteger()) {
          this.stack.push(list[i].getInteger())
        } else {
          dfs(list[i].getList())
        }
      }
    };
    dfs(nestedList);

};


