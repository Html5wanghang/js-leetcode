/**
 * Definition for singly-linked list.
 * function ListNode(val) {
 *     this.val = val;
 *     this.next = null;
 * }
 */
/**
 * @param {ListNode} head
 * @return {ListNode}
 */
var reverseList = function(head) {
    let node = new ListNode(0);
    let temp = node;
    let arr = [];
    while (head !== null) {
        arr.push(head.val);
        head = head.next;
    }
    while (arr.length > 0) {
        temp.next = new ListNode(arr.pop());
        temp = temp.next;
    }
    return node.next;
};
