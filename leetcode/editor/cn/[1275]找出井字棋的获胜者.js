/**
 * @param {number[][]} moves
 * @return {string}
 */
var tictactoe = function (moves) {
  let ArrA = [0, 0, 0, 0, 0, 0, 0, 0];
  let ArrB = [0, 0, 0, 0, 0, 0, 0, 0];
  let obj = {
    '0,0': [0, 3, 6],
    '0,1': [0, 4],
    '0,2': [0, 5, 7],
    '1,0': [1, 3],
    '1,1': [1, 4, 6, 7],
    '1,2': [1, 5],
    '2,0': [2, 3, 7],
    '2,1': [2, 4],
    '2,2': [2, 5, 6],
  };
  if (moves.length < 4) {
    return 'Pending'
  }
  for (let i = 0; i < moves.length; i++) {
    if (i % 2 === 0) {
      let str = moves[i].toString();
       obj[str].map((cat) => {
         ArrA[cat] ++
       });
       if (ArrA.includes(3)) {
         return 'A'
       }
    } else {
      let str = moves[i].toString();
      obj[str].map((cat) => {
        ArrB[cat] ++
      });
      if (ArrB.includes(3)) {
        return 'B'
      }
    }
  }
  if (moves.length === 9) {
    return 'Draw'
  } else {
    return 'Pending'
  }

};
