/**
 * Definition for a binary tree node.
 * function TreeNode(val) {
 *     this.val = val;
 *     this.left = this.right = null;
 * }
 */
/**
 * @param {TreeNode} t
 * @return {string}
 */
var tree2str = function(t) {
    if (t === null) {
      return ''
    }
    let result = '';
    let dfs = (node) => {
      result += node.val;
      if (node.right !== null && node.left === null) {
        result += '()';
      }
      if (node.left) {
        result += '('
        dfs(node.left)
        result += ')';
      }
      if (node.right) {
        result += '(';

        dfs(node.right)
        result += ')';
      }
    };
    dfs(t);
    return result
};
