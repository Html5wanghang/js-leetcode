/**
 * @param {number[]} arr
 * @return {boolean}
 */
var validMountainArray = function(arr) {
    let more = false; let less = false;
    for (let i = 0; i < arr.length - 1; i ++) {
        if (arr[i] === arr[i + 1]) {
            return false
        } else if (arr[i] > arr[i + 1]) {
            if (more === false) {
                return false
            } else if (less === false) {
                less = true
            }
        } else {
            if (more === false) {
                more = true
            } else if (less === true) {
                return false
            }
        }
    }
    return more && less
};
