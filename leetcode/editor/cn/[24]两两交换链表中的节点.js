/**
 * Definition for singly-linked list.
 * function ListNode(val, next) {
 *     this.val = (val===undefined ? 0 : val)
 *     this.next = (next===undefined ? null : next)
 * }
 */
/**
 * @param {ListNode} head
 * @return {ListNode}
 */
var swapPairs = function(head) {
  let result = new ListNode(0);
  let temp = result;
  let start, end;
  while (head !== null) {
    if (head.next === null) {
      start = new ListNode(head.val);
      temp.next = start;
      head = head.next;
    } else {
      start = new ListNode(head.val);
      head = head.next;
      end = new ListNode(head.val);
      temp.next = end;
      temp = temp.next;
      temp.next = start;
      temp = temp.next;
      head = head.next;
    }
  }
  return result.next

};
