/**
 * @param {string[]} words
 * @param {number} k
 * @return {string[]}
 */
var topKFrequent = function(words, k) {
    let arr = [];
    let map = new Map();
    for (let i = 0; i < words.length; i ++) {
        if (map.has(words[i])) {
            map.set(words[i], map.get(words[i]) + 1)
        } else {
            map.set(words[i], 1)
        }
    }
    [...map].sort((a,b) => {
        if (a[1] === b[1]) {
            return a[0] > b[0] ? 1 : -1
        } else {
            return b[1] - a[1]
        }
    }).map((cat, index) => {
        if (index < k) {
            arr.push(cat[0])
        }
    })
    return arr;
};
