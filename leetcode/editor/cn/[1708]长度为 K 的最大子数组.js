/**
 * @param {number[]} nums
 * @param {number} k
 * @return {number[]}
 */
var largestSubarray = function(nums, k) {
    let j = nums.length - k;
    let num = 0; let max = - Infinity;
    for (let i = 0; i <= j; i ++) {
        if (nums[i] > max) {
            num = i;
            max = nums[i]
        }
    };
    return nums.slice(num, k + num);
};
