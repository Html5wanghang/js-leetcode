/**
 * @param {number[]} arr1
 * @param {number[]} arr2
 * @return {number[]}
 */
var relativeSortArray = function(arr1, arr2) {
    let hashMap = new Map ();
    for (let i = 0; i < arr2.length; i ++ ){
        hashMap.set(arr2[i], i)
    }
    arr1.sort((a,b) => {
        if (hashMap.has(a) && hashMap.has(b)) {
            return hashMap.get(a) - hashMap.get(b)
        } else if (hashMap.has(a) && !hashMap.has(b)) {
            return -1
        } else if (!hashMap.has(a) && hashMap.has(b)) {
            return 1
        } else {
            return a - b
        }
    })
    return arr1
};
